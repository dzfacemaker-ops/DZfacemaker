const leaguesList = document.getElementById('leagues-list');
const displayGrid = document.getElementById('display-grid');
const playerModal = document.getElementById('player-modal');
const breadcrumb = document.getElementById('breadcrumb');
const searchInput = document.getElementById('search-input');
const backToHomeBtn = document.getElementById('back-to-home');

function init() {
    if(leaguesList) {
        leaguesList.innerHTML = '';
        Object.keys(database).forEach(league => {
            const li = document.createElement('li');
            li.innerHTML = `<i class="far fa-circle-dot" style="margin-right:8px; font-size:0.8rem;"></i> ${league}`;
            li.onclick = () => showClubs(league);
            leaguesList.appendChild(li);
        });
    }
    showAllLatestFaces();
    
    document.getElementById('close-modal-btn').onclick = () => playerModal.style.display = 'none';
    backToHomeBtn.onclick = showAllLatestFaces;
}

function createPlayerCard(player, clubName) {
    const card = document.createElement('div');
    card.className = 'face-card';
    card.innerHTML = `
        <div class="card-img-holder">
            <img src="${player.img}" alt="${player.name}" onerror="this.src='https://via.placeholder.com/220x200?text=No+Preview'">
        </div>
        <div class="card-details">
            <div>
                <h3>${player.name}</h3>
                <p class="card-club"><i class="fas fa-shield-halved"></i> ${clubName}</p>
            </div>
            <button class="card-btn">View Details</button>
        </div>
    `;
    card.querySelector('.card-btn').onclick = () => openPlayerModal(player, clubName);
    return card;
}

function showAllLatestFaces() {
    displayGrid.innerHTML = '';
    breadcrumb.textContent = "Latest Premium Faces";
    backToHomeBtn.style.display = 'none';
    searchInput.value = '';

    let hasPlayers = false;
    for (const league in database) {
        for (const club in database[league]) {
            database[league][club].forEach(player => {
                hasPlayers = true;
                displayGrid.appendChild(createPlayerCard(player, club));
            });
        }
    }
    if(!hasPlayers) displayGrid.innerHTML = '<p style="color:#888; text-align:center; width:100%;">No faces uploaded yet.</p>';
}

function showClubs(leagueName) {
    displayGrid.innerHTML = '';
    breadcrumb.textContent = leagueName;
    backToHomeBtn.style.display = 'inline-block';
    searchInput.value = '';

    const clubs = Object.keys(database[leagueName]);
    clubs.forEach(club => {
        const div = document.createElement('div');
        div.className = 'grid-item';
        div.innerHTML = `<i class="fas fa-shield-halved" style="color:#00e676; display:block; font-size:1.5rem; margin-bottom:10px;"></i> ${club}`;
        div.onclick = () => showPlayers(leagueName, club);
        displayGrid.appendChild(div);
    });
}

function showPlayers(leagueName, clubName) {
    displayGrid.innerHTML = '';
    breadcrumb.textContent = `${leagueName} > ${clubName}`;

    const players = database[leagueName][clubName];
    if(players.length === 0) {
        displayGrid.innerHTML = '<p style="color:#888; text-align:center; width:100%;">No faces available for this club.</p>';
        return;
    }
    players.forEach(player => {
        displayGrid.appendChild(createPlayerCard(player, clubName));
    });
}

function openPlayerModal(player, clubName) {
    document.getElementById('player-img').src = player.img;
    document.getElementById('player-name').textContent = player.name;
    document.getElementById('player-club-tag').querySelector('span').textContent = clubName;
    document.getElementById('player-info').textContent = player.details;
    document.getElementById('download-link').href = player.link;
    playerModal.style.display = 'flex';
}

if(searchInput) {
    searchInput.addEventListener('input', (e) => {
        const query = e.target.value.toLowerCase().trim();
        if(query === '') { showAllLatestFaces(); return; }

        displayGrid.innerHTML = '';
        breadcrumb.textContent = `Search results for: "${query}"`;
        backToHomeBtn.style.display = 'inline-block';

        let found = false;
        for (const league in database) {
            for (const club in database[league]) {
                database[league][club].forEach(player => {
                    if(player.name.toLowerCase().includes(query)) {
                        found = true;
                        displayGrid.appendChild(createPlayerCard(player, club));
                    }
                });
            }
        }
        if(!found) displayGrid.innerHTML = '<p style="color:#aaafab; text-align:center; width:100%; margin-top:20px;">No matching players found.</p>';
    });
}

window.onload = init;
